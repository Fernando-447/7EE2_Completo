package modelo;

/**
 *
 * @author carde
 */
public class HerramientaConsumibleModelo {
    private String cveMatCon;
    private String idLoc;
    private String desMatCon;
    private String estMatCon;
    private int tipMat;
    private String nomTip;

    public HerramientaConsumibleModelo() {
        this.cveMatCon = cveMatCon;
        this.idLoc = idLoc;
        this.desMatCon = desMatCon;
        this.estMatCon = estMatCon;
        this.tipMat = tipMat;
        this.nomTip = nomTip;
    }

    public String getCveMatCon() {
        return cveMatCon;
    }

    public void setCveMatCon(String cveMatCon) {
        this.cveMatCon = cveMatCon;
    }

    public String getIdLoc() {
        return idLoc;
    }

    public void setIdLoc(String idLoc) {
        this.idLoc = idLoc;
    }

    public String getDesMatCon() {
        return desMatCon;
    }

    public void setDesMatCon(String desMatCon) {
        this.desMatCon = desMatCon;
    }

    public String getEstMatCon() {
        return estMatCon;
    }

    public void setEstMatCon(String estMatCon) {
        this.estMatCon = estMatCon;
    }

    public int getTipMat() {
        return tipMat;
    }

    public void setTipMat(int tipMat) {
        this.tipMat = tipMat;
    }

    public String getNomTip() {
        return nomTip;
    }

    public void setNomTip(String nomTip) {
        this.nomTip = nomTip;
    }
    
    
}
