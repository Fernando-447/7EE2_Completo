    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        vistaModificarConsumible vistaModi = new vistaModificarConsumible();
        vistaModificarNoConsumible vistaModiNo = new vistaModificarNoConsumible();
        if(jRBConsumible.isSelected()){
            ModificarConsumibleControlador modi = new ModificarConsumibleControlador(vistaModi);
            String cveMatCon = modi.extraerCveMatCon(this);
            
            vistaModi.setVisible(true);
            modi.cargarSeleccionadoCon(cveMatCon);
            
        } else {
            if(jRBNoConsumible.isSelected()){
                ModificarNoConsumibleControlador modiNo = new ModificarNoConsumibleControlador(vistaModiNo);
                String idUTNG = modiNo.extraeridUTNG(this);
                
                vistaModiNo.setVisible(true);
                modiNo.cargarSeleccionadoNoCon(idUTNG);
            }
        }

    }//GEN-LAST:event_btnModificarActionPerformed

