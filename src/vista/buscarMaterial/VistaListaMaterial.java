    public String getClaveSeleccionada(){
        int filaSeleccionada = tblMaterial.getSelectedRow();
        if(filaSeleccionada == -1) {
            return null;
        }
        return (String) tblMaterial.getValueAt(filaSeleccionada, 0);
    }
      
    public void mostrarConfirmacionEliminar(Runnable onConfirm){
        int respuesta = JOptionPane.showConfirmDialog(
        this, "¿Está seguro que desea eliminar este registro?",
                "Confirmar Eliminación",
                JOptionPane.YES_NO_OPTION);
        
        if(respuesta == JOptionPane.YES_NO_OPTION){
            onConfirm.run();
        }
    }
    
