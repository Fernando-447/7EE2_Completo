    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        
        HerramientaConsumibleModelo h = new HerramientaConsumibleModelo();
        String cveMatCon = this.getCveMatCon().trim();
        if (cveMatCon.isEmpty()){
            JOptionPane.showMessageDialog(this, "La clave es obligatoria","Error", JOptionPane.ERROR_MESSAGE);
        }
        AltaConsumibleControlador alta = new AltaConsumibleControlador(this);
        try {
            alta.registrarHerramienta(h);
        } catch (SQLException ex) {
            Logger.getLogger(vistaAltaHerramientaConsumible.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        limpiarCampos();
        
    }//GEN-LAST:event_btnAgregarActionPerformed

