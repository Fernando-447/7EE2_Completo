    public HerramientaConsumibleModelo getDatosIngresados(){
        String estado = (String) cmbEstado.getSelectedItem();
        
        HerramientaConsumibleModelo herramienta = new HerramientaConsumibleModelo();
        
        herramienta.setCveMatCon(getCveMatCon().trim());
        herramienta.setIdLoc(getIdLocal());
        herramienta.setDesMatCon(getDescripcion().trim());
        herramienta.setEstMatCon(estado);
        
        return herramienta;
    }
